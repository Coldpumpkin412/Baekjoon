import java.util.*;

public class Main {
	public static void main(String[] args) {
        /*
         * for문(반복문)이 1부터 n까지 반복적으로 돌기 때문에
         * n만큼의 시간복잡도가 나온다
         */
		Scanner sc = new Scanner(System.in);
        
		int n = sc.nextInt();
        
		System.out.println(n);
		System.out.println('1');
	}
}